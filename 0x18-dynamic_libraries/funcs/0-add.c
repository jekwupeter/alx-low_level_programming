/**
 * add - adds two numbers
 * @a: first input number
 * @b: second input number
 *
 * Return: sum of two digits
 *
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - finds the difference btw two numbers
 * @a: first input number
 * @b: second input number
 *
 * Return: difference of two digits
 *
 */
int sub(int a, int b)
{
        return (a - b);
}
/**
 * mul - multiplies two numbers
 * @a: first input number
 * @b: second input number
 *
 * Return: multiplication result of two digits
 *
 */
int mul(int a, int b)
{
        return (a * b);
}
/**
 * div - divides two numbers
 * @a: first input number
 * @b: second input number
 *
 * Return: division of two digits
 *
 */
int div(int a, int b)
{
        return (a / b);
}
/**
 * mod - finds modulus two numbers
 * @a: first input number
 * @b: second input number
 *
 * Return: modulus of two digits
 *
 */
int mod(int a, int b)
{
        return (a % b);
}
